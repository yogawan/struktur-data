# fungsi input
def birth(tpt,tgl,bln,thn):
    print(f"tempat lahir : {tpt}")
    print(f"tanggal lahir : {tgl}")
    print(f"bulan lahir : {bln}")
    print(f"tahun lahir : {thn}")
    
tpt = input("masukan tempat lahir : ")
tgl = input("masukan tanggal lahir : ")
bln = input("masukan bulan lahir : ")
thn = input("masukan tahun lahir : ")
birth(tpt,tgl,bln,thn)